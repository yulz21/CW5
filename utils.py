def select_companies():
    number_of_companies = int(input("Укажите числом: для скольки компаний вы хотите осуществить поиск: "))
    companies = []
    for comp in range(number_of_companies):
        keyword = input("Введите название компании для поиска вакансий: ").lower()
        if len(keyword) > 0:
            companies.append(keyword)
    return companies


